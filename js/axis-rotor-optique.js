(function () {
  "use strict";

  var imagesMeta = Array.isArray(window.AXIS_ROTOR_IMAGES) ? window.AXIS_ROTOR_IMAGES : [];
  var loadedImages = [];
  var canvas;
  var ctx;
  var running = true;
  var lastTime = performance.now();
  var angle = 0;

  function byId(id) {
    return document.getElementById(id);
  }

  function clamp(value, min, max, fallback) {
    var n = Number(value);
    if (Number.isNaN(n)) return fallback;
    return Math.max(min, Math.min(max, n));
  }

  function loadImage(meta) {
    return new Promise(function (resolve) {
      var img = new Image();
      img.onload = function () {
        resolve(Object.assign({}, meta, { img: img, ok: true }));
      };
      img.onerror = function () {
        resolve(Object.assign({}, meta, { img: null, ok: false }));
      };
      img.src = meta.src;
    });
  }

  function fillImageSelects() {
    var blade = byId("rotorBladeImage");
    var center = byId("rotorCenterImage");

    if (!blade || !center) return;

    blade.innerHTML = "";
    center.innerHTML = "";

    var all = document.createElement("option");
    all.value = "__all__";
    all.textContent = "Toutes les images — une par pale";
    blade.appendChild(all);

    imagesMeta.forEach(function (meta, index) {
      var opt1 = document.createElement("option");
      opt1.value = meta.id;
      opt1.textContent = meta.label || meta.id;
      blade.appendChild(opt1);

      var opt2 = document.createElement("option");
      opt2.value = meta.id;
      opt2.textContent = meta.label || meta.id;
      center.appendChild(opt2);

      if (index === 0) {
        center.value = meta.id;
      }
    });

    blade.value = "__all__";
  }

  function getImageById(id) {
    return loadedImages.find(function (item) {
      return item.id === id && item.ok && item.img;
    }) || null;
  }

  function getConfig() {
    return {
      bladeImage: byId("rotorBladeImage") ? byId("rotorBladeImage").value : "__all__",
      centerImage: byId("rotorCenterImage") ? byId("rotorCenterImage").value : "",
      direction: byId("rotorDirection") ? byId("rotorDirection").value : "clockwise",
      speed: clamp(byId("rotorSpeed") ? byId("rotorSpeed").value : 12, 0, 30, 12),
      blades: clamp(byId("rotorBlades") ? byId("rotorBlades").value : 8, 3, 12, 8),
      colorInner: byId("rotorInnerColor") ? byId("rotorInnerColor").value : "#ffd36b",
      colorOuter: byId("rotorOuterColor") ? byId("rotorOuterColor").value : "#6cb9ff"
    };
  }

  function hexToRgb(hex) {
    var h = String(hex || "#ffffff").replace("#", "");
    if (h.length === 3) {
      h = h.split("").map(function (c) { return c + c; }).join("");
    }

    var n = parseInt(h, 16);

    return {
      r: (n >> 16) & 255,
      g: (n >> 8) & 255,
      b: n & 255
    };
  }

  function rgba(hex, alpha) {
    var c = hexToRgb(hex);
    return "rgba(" + c.r + "," + c.g + "," + c.b + "," + alpha + ")";
  }

  function drawImageCircle(img, x, y, size) {
    if (!img) return;

    ctx.save();
    ctx.beginPath();
    ctx.arc(x, y, size / 2, 0, Math.PI * 2);
    ctx.clip();

    var ratio = Math.max(size / img.width, size / img.height);
    var w = img.width * ratio;
    var h = img.height * ratio;

    ctx.drawImage(img, x - w / 2, y - h / 2, w, h);
    ctx.restore();

    ctx.save();
    ctx.beginPath();
    ctx.arc(x, y, size / 2, 0, Math.PI * 2);
    ctx.lineWidth = Math.max(2, size * 0.045);
    ctx.strokeStyle = "rgba(255,245,214,0.76)";
    ctx.stroke();

    ctx.beginPath();
    ctx.arc(x, y, size / 2 + size * 0.12, 0, Math.PI * 2);
    ctx.strokeStyle = "rgba(216,180,95,0.24)";
    ctx.lineWidth = Math.max(1, size * 0.035);
    ctx.stroke();
    ctx.restore();
  }

  function drawBackground(w, h) {
    var grad = ctx.createRadialGradient(w / 2, h / 2, 20, w / 2, h / 2, w * 0.62);
    grad.addColorStop(0, "rgba(25,32,52,1)");
    grad.addColorStop(0.52, "rgba(6,10,18,1)");
    grad.addColorStop(1, "rgba(2,4,8,1)");
    ctx.fillStyle = grad;
    ctx.fillRect(0, 0, w, h);

    ctx.save();
    ctx.globalAlpha = 0.22;
    for (var i = 0; i < 90; i += 1) {
      var x = (Math.sin(i * 41.3) * 0.5 + 0.5) * w;
      var y = (Math.cos(i * 27.7) * 0.5 + 0.5) * h;
      ctx.beginPath();
      ctx.arc(x, y, 1.1, 0, Math.PI * 2);
      ctx.fillStyle = "rgba(255,231,163,0.72)";
      ctx.fill();
    }
    ctx.restore();
  }

  function drawRotor() {
    if (!canvas || !ctx) return;

    var cfg = getConfig();
    var rect = canvas.getBoundingClientRect();
    var size = Math.max(720, Math.floor(rect.width * window.devicePixelRatio));
    canvas.width = size;
    canvas.height = size;

    var w = canvas.width;
    var h = canvas.height;
    var cx = w / 2;
    var cy = h / 2;

    drawBackground(w, h);

    var base = Math.min(w, h);
    var outer = base * 0.445;
    var ring = outer * 0.92;
    var rotorRadius = outer * 0.72;
    var hub = outer * 0.145;
    var bladeOrbit = rotorRadius * 0.92;
    var tipOrbit = rotorRadius * 0.98;
    var bladeCount = cfg.blades;
    var speed = cfg.speed;
    var direction = cfg.direction === "clockwise" ? 1 : -1;

    var aura = ctx.createRadialGradient(cx, cy, outer * 0.15, cx, cy, outer * 1.12);
    aura.addColorStop(0, rgba(cfg.colorInner, 0.30));
    aura.addColorStop(0.55, rgba(cfg.colorOuter, 0.12));
    aura.addColorStop(1, "rgba(0,0,0,0)");
    ctx.fillStyle = aura;
    ctx.beginPath();
    ctx.arc(cx, cy, outer * 1.16, 0, Math.PI * 2);
    ctx.fill();

    ctx.save();
    ctx.translate(cx, cy);

    ctx.beginPath();
    ctx.arc(0, 0, outer, 0, Math.PI * 2);
    ctx.fillStyle = "rgba(2,5,10,0.76)";
    ctx.fill();
    ctx.lineWidth = outer * 0.032;
    ctx.strokeStyle = rgba(cfg.colorOuter, 0.76);
    ctx.stroke();

    ctx.beginPath();
    ctx.arc(0, 0, ring, 0, Math.PI * 2);
    ctx.lineWidth = outer * 0.010;
    ctx.strokeStyle = "rgba(255,245,214,0.18)";
    ctx.stroke();

    for (var tick = 0; tick < 60; tick += 1) {
      var a = (tick / 60) * Math.PI * 2;
      var major = tick % 5 === 0;
      var inner = outer * (major ? 0.86 : 0.89);
      var outerTick = outer * 0.965;

      ctx.beginPath();
      ctx.moveTo(Math.cos(a) * inner, Math.sin(a) * inner);
      ctx.lineTo(Math.cos(a) * outerTick, Math.sin(a) * outerTick);
      ctx.lineWidth = major ? outer * 0.008 : outer * 0.004;
      ctx.strokeStyle = major ? "rgba(255,245,214,0.50)" : "rgba(255,255,255,0.16)";
      ctx.stroke();
    }

    ctx.rotate(angle);

    for (var i = 0; i < bladeCount; i += 1) {
      var bladeAngle = (i / bladeCount) * Math.PI * 2;
      ctx.save();
      ctx.rotate(bladeAngle);

      var bladeGradient = ctx.createLinearGradient(0, -hub, 0, -bladeOrbit);
      bladeGradient.addColorStop(0, "rgba(255,255,255,0.06)");
      bladeGradient.addColorStop(0.35, rgba(cfg.colorInner, 0.26));
      bladeGradient.addColorStop(1, "rgba(255,255,255,0.04)");

      ctx.fillStyle = bladeGradient;
      ctx.beginPath();
      ctx.moveTo(-outer * 0.034, -hub * 0.6);
      ctx.quadraticCurveTo(-outer * 0.085, -outer * 0.32, -outer * 0.040, -bladeOrbit);
      ctx.lineTo(outer * 0.040, -bladeOrbit);
      ctx.quadraticCurveTo(outer * 0.085, -outer * 0.32, outer * 0.034, -hub * 0.6);
      ctx.closePath();
      ctx.fill();

      ctx.lineWidth = Math.max(2, outer * 0.006);
      ctx.strokeStyle = "rgba(255,245,214,0.18)";
      ctx.stroke();

      var imageToDraw = null;

      if (cfg.bladeImage === "__all__") {
        var valid = loadedImages.filter(function (img) { return img.ok && img.img; });
        if (valid.length) {
          imageToDraw = valid[i % valid.length];
        }
      } else {
        imageToDraw = getImageById(cfg.bladeImage);
      }

      if (imageToDraw && imageToDraw.img) {
        drawImageCircle(imageToDraw.img, 0, -tipOrbit, outer * 0.19);
      } else {
        ctx.beginPath();
        ctx.arc(0, -tipOrbit, outer * 0.075, 0, Math.PI * 2);
        ctx.fillStyle = rgba(cfg.colorInner, 0.88);
        ctx.fill();
      }

      ctx.restore();
    }

    ctx.restore();

    var centerImage = getImageById(cfg.centerImage);

    ctx.save();
    ctx.translate(cx, cy);

    var hubGrad = ctx.createRadialGradient(-hub * 0.25, -hub * 0.25, 4, 0, 0, hub * 1.35);
    hubGrad.addColorStop(0, "#fffef6");
    hubGrad.addColorStop(0.42, rgba(cfg.colorInner, 0.96));
    hubGrad.addColorStop(1, rgba(cfg.colorOuter, 0.72));

    ctx.beginPath();
    ctx.arc(0, 0, hub * 1.15, 0, Math.PI * 2);
    ctx.fillStyle = hubGrad;
    ctx.fill();
    ctx.lineWidth = Math.max(2, outer * 0.010);
    ctx.strokeStyle = "rgba(255,245,214,0.62)";
    ctx.stroke();

    if (centerImage && centerImage.img) {
      drawImageCircle(centerImage.img, 0, 0, hub * 1.48);
    } else {
      ctx.beginPath();
      ctx.arc(0, 0, hub * 0.32, 0, Math.PI * 2);
      ctx.fillStyle = "rgba(5,9,16,0.82)";
      ctx.fill();
    }

    ctx.restore();

    var status = byId("rotorStatus");
    if (status) {
      var label = "Toutes les images";
      if (cfg.bladeImage !== "__all__") {
        var chosen = imagesMeta.find(function (item) { return item.id === cfg.bladeImage; });
        label = chosen ? chosen.label : cfg.bladeImage;
      }

      status.innerHTML =
        "<strong>État :</strong> " +
        (running ? "rotation active" : "rotation arrêtée") +
        " · vitesse " + cfg.speed + "/30" +
        " · " + cfg.blades + " pales" +
        " · images en bout de pales : " + label + ".";
    }
  }

  function loop(now) {
    var cfg = getConfig();
    var dt = Math.min(0.05, (now - lastTime) / 1000);
    lastTime = now;

    if (running) {
      var direction = cfg.direction === "clockwise" ? 1 : -1;
      var turnsPerSecond = cfg.speed * 0.045;
      angle += direction * turnsPerSecond * Math.PI * 2 * dt;
    }

    drawRotor();
    requestAnimationFrame(loop);
  }

  function bind() {
    [
      "rotorBladeImage",
      "rotorCenterImage",
      "rotorDirection",
      "rotorSpeed",
      "rotorBlades",
      "rotorInnerColor",
      "rotorOuterColor"
    ].forEach(function (id) {
      var el = byId(id);
      if (!el) return;
      el.addEventListener("input", drawRotor);
      el.addEventListener("change", drawRotor);
    });

    var pauseBtn = byId("rotorPause");
    var reverseBtn = byId("rotorReverse");
    var fullBtn = byId("rotorFullscreen");

    if (pauseBtn) {
      pauseBtn.addEventListener("click", function () {
        running = !running;
        pauseBtn.textContent = running ? "Pause" : "Reprendre";
      });
    }

    if (reverseBtn) {
      reverseBtn.addEventListener("click", function () {
        var dir = byId("rotorDirection");
        if (!dir) return;
        dir.value = dir.value === "clockwise" ? "counterclockwise" : "clockwise";
        drawRotor();
      });
    }

    if (fullBtn) {
      fullBtn.addEventListener("click", function () {
        var target = byId("rotorCanvasWrap") || canvas;
        if (target && target.requestFullscreen) {
          target.requestFullscreen().catch(function () {});
        }
      });
    }
  }

  async function init() {
    canvas = byId("axisRotorCanvas");
    if (!canvas) return;

    ctx = canvas.getContext("2d");

    fillImageSelects();

    loadedImages = await Promise.all(imagesMeta.map(loadImage));

    bind();
    drawRotor();
    requestAnimationFrame(loop);
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
})();